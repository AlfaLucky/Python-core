class HowManyMiles:
    # empty constructor
    def __init__(self):
        pass

    # Calculate and output the number of miles in 1000 kilometers.
    # Hint: One mile is 1.6 kilometers, so find the quotient of 1000 and 1.6.
    # Use the floor division operator //.
    def division_operator(self, x1, x2):
        print(x1 // x2)
        return x1 // x2
